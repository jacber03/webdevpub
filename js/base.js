function fadeIn() {
    var x = document.getElementById("ufcFakta");
    var opacity = 0;
    function fade () {
        x.style.opacity = opacity;
        opacity += 0.01;
        if (opacity >= 1){
            x.style.opacity = 1;
            return true;
        }
        requestAnimationFrame(fade);
    }
    fade();
}